/**
 * @brief   Initialise the BMC debug access
 */
void vBMC_DebugInit( DAL_HDL pxParentHandle )
{
    if( FALSE == iIsInitialised )
    {
        if( NULL == pxParentHandle )
        {
            pxBmcTop = pxDAL_NewDirectory( "bmc" );
        }
        else
        {
            pxBmcTop = pxDAL_NewSubDirectory( "bmc", pxParentHandle );
        }

        if( NULL != pxBmcTop )
        {
            pxDAL_NewDebugFunction( "print_stats",    pxBmcTop, vPrintStats );
            pxDAL_NewDebugFunction( "clear_stats",    pxBmcTop, vClearStats );
            pxDAL_NewDebugFunction( "bind_callbacks", pxBmcTop, vBindCallbacks );
            pxDAL_NewDebugFunction( "get_state",      pxBmcTop, vGetState );
            pxMctpDir      = pxDAL_NewSubDirectory( "mctp", pxBmcTop );
            pxPldmType0Dir = pxDAL_NewSubDirectory( "pldm_type0", pxBmcTop );
            pxPldmType2Dir = pxDAL_NewSubDirectory( "pldm_type2", pxBmcTop );
            pxPldmType5Dir = pxDAL_NewSubDirectory( "pldm_type5", pxBmcTop );
        }

        if( NULL != pxMctpDir )
        {
            pxDAL_NewDebugFunction( "get_eid",                      pxMctpDir, vTestGetEid );
            pxDAL_NewDebugFunction( "set_eid_11",                   pxMctpDir, vTestSetEid );
            pxDAL_NewDebugFunction( "set_eid_21",                   pxMctpDir, vTestSetEid2 );
            pxDAL_NewDebugFunction( "GetEndpointUuid",              pxMctpDir, vTestGetEndpointUuid );
            pxDAL_NewDebugFunction( "GetMctpVersionSupport",        pxMctpDir, vTestGetMctpVersionSupport );
            pxDAL_NewDebugFunction( "GetMctpVersionSupportType1",   pxMctpDir, vTestGetMctpVersionSupportType1 );
            pxDAL_NewDebugFunction( "GetMessageTypeSupport",        pxMctpDir, vTestGetMessageTypeSupport );
        }

        if( NULL != pxPldmType0Dir )
        {
            pxDAL_NewDebugFunction( "SetTid",                       pxPldmType0Dir, vTestSetTid );
            pxDAL_NewDebugFunction( "GetTid",                       pxPldmType0Dir, vTestGetTid );
            pxDAL_NewDebugFunction( "GetPldmversion",               pxPldmType0Dir, vTestGetPldmversion );

            pxDAL_NewDebugFunction( "GetPldmversionType5",          pxPldmType0Dir, vTestGetPldmversionType5 );
            pxDAL_NewDebugFunction( "GetPLDMtypes",                 pxPldmType0Dir, vTestGetPLDMtypes );
            pxDAL_NewDebugFunction( "GetPLDMcommands",              pxPldmType0Dir, vTestGetPLDMcommands );
        }

        if( NULL != pxPldmType2Dir )
        {
            pxDAL_NewDebugFunction( "GetPldmversionType2",          pxPldmType2Dir, vTestGetPldmversionType2 );
            pxDAL_NewDebugFunction( "GetPLDMcommandsType2",         pxPldmType2Dir, vTestGetPLDMcommandsType2 );
            pxDAL_NewDebugFunction( "getPDRRepositoryInfo",         pxPldmType2Dir, vTestGetPDRRepositoryInfo );
            pxDAL_NewDebugFunction( "getPDR",                       pxPldmType2Dir, vTestGetPDR );
            pxDAL_NewDebugFunction( "getSensor",                    pxPldmType2Dir, vTestGetSensorReading );
            pxDAL_NewDebugFunction( "SetSensorEnable",              pxPldmType2Dir, vTestSetNumericSensorEnable );

        }

        if( NULL != pxPldmType5Dir )
        {
            pxDAL_NewDebugFunction( "GetPLDMcommandsType5",         pxPldmType5Dir, vTestGetPLDMcommandsType5 );
        }

        iIsInitialised = TRUE;
    }
}

/**
 * @brief   Debug function to print this module's stats
 */
static void vPrintStats( void )
{
    if( OK != iBMC_PrintStatistics() )
    {
        PLL_DAL( BMC_DBG_NAME, "Error printing statistics\r\n" );
    }
}

/**
 * @brief   Debug function to clear this module's stats
 */
static void vClearStats( void )
{
    if( OK != iBMC_ClearStatistics() )
    {
        PLL_DAL( BMC_DBG_NAME, "Error clearing statistics\r\n" );
    }
}

/**
 * @brief   Debug function to bind a callback to this module
 */
static void vBindCallbacks( void )
{
    if( OK != iBMC_BindCallback( &iTestCallback ) )
    {
        PLL_DAL( BMC_DBG_NAME, "Error binding callback\r\n" );
    }
    else
    {
        PLL_DAL( BMC_DBG_NAME, "Callback bound\r\n" );
    }
}

/**
 * @brief   Debug function to get state of BMC
 */
static void vGetState( void )
{
    MODULE_STATE xState = MODULE_STATE_OK;

    if( OK != iBMC_GetState( &xState ) )
    {
        PLL_DAL( BMC_DBG_NAME, "Error getting state\r\n" );
    }
    else
    {
        PLL_DAL( BMC_DBG_NAME, "0: MODULE_STATE_OK 1: MODULE_STATE_UNINITIALISED 2: MODULE_STATE_ERROR\r\n" );
        PLL_DAL( BMC_DBG_NAME, "Successfully retrieved state: %d\r\n", xState );
    }
}

/**
 * @brief   EVL Callback for binding test prints
 */
static int iTestCallback( EVL_SIGNAL *pxSignal )
{
    int iStatus = ERROR;

    if( NULL != pxSignal )
    {
        PLL_DAL( BMC_DBG_NAME,
                 "BMC Signal raised: Module[%d], Type[%d], Instance[%d]\r\n",
                 pxSignal->ucModule,
                 pxSignal->ucEventType,
                 pxSignal->ucInstance );
        iStatus = OK;
    }

    return iStatus;
}

/**
 * @brief   Test the the GetEID Message
 */
static void vTestGetEid( void )
{
    /*
       0F – MCTP Command Code
       08 – Byte Count
       21  -  Source Slave Address (0x20 (0x10 - 7 Bit address ) Ored with 1
       01 – MCTP Header version
       00 – Destn Endpoint
       09 – Source Endpoint
       C8 – Bitwise values ->  [[7] SOM: Start Of Message flag (*) [6] EOM: End Of Message flag (*) [5:4] Packet sequence number (*) [3] Tag Owner (TO) bit (*) [2:0] Message tag (*)]
       00 – MSG Type
       9E – MSG Header
       02 – MCTP Command Code GetEID
       73 - PEC
     */

    uint16_t usEidMessageSize = GET_EID_MESSAGE_SIZE;
    uint8_t  pucEidMessageData[ GET_EID_MESSAGE_SIZE ] =
    {
        0x21, 0x01, 0x00, 0x09, 0xC8, 0x00, 0x9E, 0x02
    };

    PLL_DAL( BMC_DBG_NAME, "Get EID { 0x0F, 0x08, 0x21, 0x01, 0x00, 0x09, 0xC8, 0x00, 0x9E, 0x02, 0x73 }" );
    vEmulateReceivedMessage( pucEidMessageData, usEidMessageSize );
    iOSAL_Task_SleepMs( 1000 );
}

